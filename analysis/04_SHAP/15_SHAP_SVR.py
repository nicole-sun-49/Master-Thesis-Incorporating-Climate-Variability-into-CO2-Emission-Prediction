def _load_tvt(path: str, feature_set: str) -> pd.DataFrame:
    """
    Load a train/val/test MAE CSV and return per-country rows for one
    feature set, in the order defined by COUNTRIES.
    Skips rows that are summary averages (containing 'AVG').
    """
    df = pd.read_csv(path)
    df = df[~df["country"].str.contains("AVG", na=False)].copy()
    df = df[df["feature_set"] == feature_set].copy()
    df["country"] = pd.Categorical(df["country"], categories=COUNTRIES, ordered=True)
    return df.sort_values("country").reset_index(drop=True)


def plot_regularization_bar(output_dir: str) -> None:
    """
    Bar chart showing Train / Val / Test MAE and Generalization Gap
    before and after regularization, averaged across all 6 countries.

    One chart per feature set (baseline, augmented) → 2 PNGs saved.

    Layout
    ------
    X-axis  : 4 groups  —  Train | Val | Test | Gap
    Y-axis  : MAE (scaled y, range 0–1)
    Bars    : Original (light) vs Regularized (dark), 2 bars per group
    Gap     : defined as  Test MAE − Train MAE  per country, then averaged

    Files saved  (→ comparison_charts/)
    -----------
    regularization_bar_baseline.png
    regularization_bar_augmented.png
    """
    if not os.path.exists(ORIG_TVT_PATH) or not os.path.exists(REG_TVT_PATH):
        print("  [SKIP] Regularization bar chart: one or both TVT CSVs not found.")
        return

    save_dir = os.path.join(output_dir, "comparison_charts")
    ensure_dir(save_dir)

    for fset in ["baseline", "augmented"]:
        orig = _load_tvt(ORIG_TVT_PATH, fset)
        reg  = _load_tvt(REG_TVT_PATH,  fset)

        def _avgs(df):
            gap = df["test_MAE_scaled_y"] - df["train_MAE_scaled_y"]
            return {
                "Train": df["train_MAE_scaled_y"].mean(),
                "Val":   df["val_MAE_scaled_y"].mean(),
                "Test":  df["test_MAE_scaled_y"].mean(),
                "Gap":   gap.mean(),
            }

        orig_vals = _avgs(orig)
        reg_vals  = _avgs(reg)

        labels   = list(orig_vals.keys())
        x        = np.arange(len(labels))
        width    = 0.35

        fig, ax = plt.subplots(figsize=(8, 5))

        bars_orig = ax.bar(x - width / 2, [orig_vals[l] for l in labels],
                           width, label="Original",     color=COLOR_ORIG,
                           edgecolor="white", linewidth=0.6)
        bars_reg  = ax.bar(x + width / 2, [reg_vals[l]  for l in labels],
                           width, label="Regularized",  color=COLOR_REG,
                           edgecolor="white", linewidth=0.6)

        for bars in (bars_orig, bars_reg):
            for bar in bars:
                h = bar.get_height()
                ax.text(bar.get_x() + bar.get_width() / 2, h + 0.002,
                        f"{h:.4f}", ha="center", va="bottom", fontsize=8)

        feature_label = "Baseline" if fset == "baseline" else "Augmented"
        ax.set_title(
            f"SVR {feature_label} — Average MAE Before vs After Regularization\n"
            f"(averaged over {len(COUNTRIES)} countries)",
            fontsize=11
        )
        ax.set_xticks(x)
        ax.set_xticklabels(labels, fontsize=10)
        ax.set_ylabel("MAE (scaled y)", fontsize=10)
        ax.set_ylim(0, max(max(orig_vals.values()), max(reg_vals.values())) * 1.25)
        ax.legend(fontsize=9)
        ax.grid(axis="y", linestyle="--", alpha=0.4)
        ax.spines[["top", "right"]].set_visible(False)
        ax.text(0.99, 0.97, "Gap = Test MAE − Train MAE",
                transform=ax.transAxes, fontsize=7.5, color="gray",
                ha="right", va="top")

        fig.tight_layout()
        fname = os.path.join(save_dir, f"regularization_bar_{fset}.png")
        fig.savefig(fname, dpi=150, bbox_inches="tight")
        plt.close(fig)
        print(f"  Saved: comparison_charts/regularization_bar_{fset}.png")


