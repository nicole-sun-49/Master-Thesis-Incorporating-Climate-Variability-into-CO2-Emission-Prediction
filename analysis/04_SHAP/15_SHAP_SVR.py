def main():
    # ── Create all output sub-directories upfront ─────────
    dirs = setup_subdirs(OUTPUT_DIR)

    # ── Section 1: Static comparison charts ───────────────
    # These run first because they only read CSVs and are fast.
    # They do NOT require the SHAP loop to complete first.
    print("\n[1/4] Regularization comparison bar charts (6-country average) …")
    plot_regularization_bar(OUTPUT_DIR)

    print("\n[2/4] Per-country regularization bar charts (12 charts) …")
    plot_per_country_regularization_bar(OUTPUT_DIR)

    print("\n[3/4] Hyperparameter comparison tables …")
    plot_hyperparameter_table(OUTPUT_DIR)

    # ── Section 2: SHAP loop (original logic, unchanged) ──
    print("\n[4/4] Running SHAP analysis per country …")
    data          = pd.read_csv(DATA_PATH)
    best_params_df = load_best_params(BEST_PARAMS_PATH)

    run_logs              = []
    all_importance_tables = []
    all_morf_lerf_tables  = []

    data_dir = dirs["data"]   # aggregated CSVs go here

    for country in COUNTRIES:
        for feature_set_name, feature_cols in [("augmented", FEATURE_SETS["augmented"])]:
            try:
                log_row = run_svr_shap_for_country(
                    data            = data,
                    best_params_df  = best_params_df,
                    country_name    = country,
                    feature_set_name= feature_set_name,
                    feature_cols    = feature_cols,
                    target_col      = TARGET_COL,
                    output_dir      = OUTPUT_DIR,
                )
                run_logs.append(log_row)

                country_safe = country.replace(" ", "_")
                tag = f"{country_safe}_{feature_set_name}"

                # importance CSVs are now in data/
                imp_path = os.path.join(data_dir, f"{tag}_shap_importance.csv")
                if os.path.exists(imp_path):
                    all_importance_tables.append(pd.read_csv(imp_path))

                # morf/lerf CSVs are now in morf_lerf/
                ml_path = os.path.join(dirs["morf"], f"{tag}_morf_lerf_curve.csv")
                if os.path.exists(ml_path):
                    all_morf_lerf_tables.append(pd.read_csv(ml_path))

            except Exception as e:
                print(f"ERROR: {country} | {feature_set_name}: {e}")
                print(traceback.format_exc())
                run_logs.append({
                    "country":      country,
                    "feature_set":  feature_set_name,
                    "status":       "failed",
                    "n_rows_used":  np.nan,
                    "kernel":       np.nan,
                    "C":            np.nan,
                    "epsilon":      np.nan,
                    "gamma":        np.nan,
                    "degree":       np.nan,
                    "morf_lerf_final_MORF_MAE_scaled_y": np.nan,
                    "morf_lerf_final_LERF_MAE_scaled_y": np.nan,
                    "error_message": str(e),
                })

            # Save incrementally after each country so progress is not lost
            # if the script is interrupted mid-run
            pd.DataFrame(run_logs).to_csv(
                os.path.join(data_dir, "shap_run_log.csv"), index=False
            )
            if all_importance_tables:
                pd.concat(all_importance_tables, ignore_index=True).to_csv(
                    os.path.join(data_dir, "all_countries_shap_importance.csv"),
                    index=False,
                )
            if all_morf_lerf_tables:
                pd.concat(all_morf_lerf_tables, ignore_index=True).to_csv(
                    os.path.join(data_dir, "all_countries_morf_lerf_curves.csv"),
                    index=False,
                )

    # ── Section 3: SHAP feature summary table ─────────────
    # Runs after the loop so all_countries_shap_importance.csv is complete.
    print("\n[Post-loop] SHAP feature summary table …")
    plot_shap_feature_summary(OUTPUT_DIR)

    print("\nAll done.")
    print(f"\nOutput structure:")
    print(f"  {OUTPUT_DIR}/")
    print(f"  ├── shap_plots/        SHAP bar + summary charts (12 PNGs)")
    print(f"  ├── morf_lerf/         MoRF/LeRF curves + CSVs (12 PNGs + 6 CSVs)")
    print(f"  ├── comparison_charts/ Regularization bars (14 PNGs) + hyperparam tables (2 PNGs)")
    print(f"  ├── feature_summary/   SHAP feature summary (1 PNG + 1 CSV)")
    print(f"  └── data/              Raw SHAP values, importance CSVs, run log")


if __name__ == "__main__":
    main()
