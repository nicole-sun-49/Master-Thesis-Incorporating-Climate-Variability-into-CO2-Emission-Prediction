def plot_hyperparameter_table(output_dir: str) -> None:
    """
    Matplotlib table showing best hyperparameters for Original vs Regularized
    SVR models side by side.

    One table per feature set (baseline, augmented) → 2 PNGs saved.

    Columns
    -------
    Country | ← Original → kernel / C / ε / γ | ← Regularized → kernel / C / ε / γ

    Files saved
    -----------
    hyperparameter_table_baseline.png
    hyperparameter_table_augmented.png
    """
    if not os.path.exists(ORIG_METRICS_PATH) or not os.path.exists(BEST_PARAMS_PATH):
        print("  [SKIP] Hyperparameter table: one or both metrics CSVs not found.")
        return

    save_dir = os.path.join(output_dir, "comparison_charts")
    ensure_dir(save_dir)

    orig_df = pd.read_csv(ORIG_METRICS_PATH)
    reg_df  = pd.read_csv(BEST_PARAMS_PATH)

    # Keep only per-country rows
    orig_df = orig_df[~orig_df["country"].str.contains("AVG", na=False)].copy()
    reg_df  = reg_df[ ~reg_df["country"].str.contains("AVG", na=False)].copy()

    param_cols = ["best_kernel", "best_C", "best_epsilon", "best_gamma"]
    disp_names = ["Kernel", "C", "ε", "γ"]

    for fset in ["baseline", "augmented"]:
        o = orig_df[orig_df["feature_set"] == fset].copy()
        r = reg_df[ reg_df["feature_set"]  == fset].copy()

        o["country"] = pd.Categorical(o["country"], categories=COUNTRIES, ordered=True)
        r["country"] = pd.Categorical(r["country"], categories=COUNTRIES, ordered=True)
        o = o.sort_values("country").reset_index(drop=True)
        r = r.sort_values("country").reset_index(drop=True)

        def fmt(val):
            """Format a hyperparameter value for table display."""
            if pd.isna(val):
                return "—"
            if isinstance(val, float) and val == int(val):
                return str(int(val))
            return str(val)

        # Build table data: one row per country
        rows = []
        for i, country in enumerate(o["country"]):
            row = [country]
            for c in param_cols:
                row.append(fmt(o.loc[i, c]))
            for c in param_cols:
                row.append(fmt(r.loc[i, c]))
            rows.append(row)

        # Column headers (two groups)
        col_headers = (
            ["Country"]
            + [f"Orig {n}" for n in disp_names]
            + [f"Reg {n}"  for n in disp_names]
        )
        n_cols = len(col_headers)
        n_rows = len(rows)

        fig, ax = plt.subplots(figsize=(11, 0.55 * (n_rows + 2)))
        ax.axis("off")

        tbl = ax.table(
            cellText=rows,
            colLabels=col_headers,
            cellLoc="center",
            loc="center",
        )
        tbl.auto_set_font_size(False)
        tbl.set_fontsize(9.5)
        tbl.scale(1, 1.7)

        # Header row colouring
        orig_group_cols = range(1, 1 + len(param_cols))        # cols 1–4
        reg_group_cols  = range(1 + len(param_cols), n_cols)   # cols 5–8

        for col_idx in range(n_cols):
            cell = tbl[0, col_idx]
            cell.set_facecolor(COLOR_HEAD)
            cell.set_text_props(color="white", fontweight="bold")

        # Subtle tint to distinguish Original vs Regularized columns
        for row_idx in range(1, n_rows + 1):
            bg = "#F7F7F7" if row_idx % 2 == 0 else "white"
            for col_idx in range(n_cols):
                tbl[row_idx, col_idx].set_facecolor(bg)
            # Slightly tinted background for Regularized columns
            for col_idx in reg_group_cols:
                tinted = "#EEF4FB" if row_idx % 2 == 0 else "#F5F9FF"
                tbl[row_idx, col_idx].set_facecolor(tinted)

        # Legend patches
        orig_patch = mpatches.Patch(color=COLOR_ORIG, label="Original")
        reg_patch  = mpatches.Patch(color=COLOR_REG,  label="Regularized")
        ax.legend(
            handles=[orig_patch, reg_patch],
            loc="upper right", fontsize=8, framealpha=0.7
        )

        feature_label = "Baseline" if fset == "baseline" else "Augmented"
        ax.set_title(
            f"SVR {feature_label} — Hyperparameters: Original vs Regularized",
            fontsize=11, fontweight="bold", pad=14
        )

        fig.tight_layout()
        fname = os.path.join(save_dir, f"hyperparameter_table_{fset}.png")
        fig.savefig(fname, dpi=150, bbox_inches="tight")
        plt.close(fig)
        print(f"  Saved: comparison_charts/hyperparameter_table_{fset}.png")


def plot_shap_feature_summary(output_dir: str) -> None:
    """
    Summary table derived from all_countries_shap_importance.csv (augmented
    feature set only, regularized SVR).

    For each country the table shows:
      • Rank 1 / Rank 2 / Rank 3  — top-3 most important features by mean |SHAP|
      • Top Temp Feature           — highest-ranked temperature-related variable
      • Temp Feature Rank          — its position in the full importance ranking

    Temperature-related features are identified as any feature whose name
    contains: "Temperature", "frost", or "hot days".

    Files saved
    -----------
    shap_feature_summary_table.png
    shap_feature_summary_table.csv
    """
    imp_path = os.path.join(output_dir, "data", "all_countries_shap_importance.csv")
    if not os.path.exists(imp_path):
        # fallback: check root output dir (for backward compatibility)
        imp_path = os.path.join(output_dir, "all_countries_shap_importance.csv")
    if not os.path.exists(imp_path):
        print("  [SKIP] SHAP feature summary: all_countries_shap_importance.csv not found yet.")
        return

    save_dir = os.path.join(output_dir, "feature_summary")
    ensure_dir(save_dir)

    imp_df = pd.read_csv(imp_path)

    # Normalise feature names: replace underscores so display is consistent
    imp_df["feature_display"] = imp_df["feature"].str.replace("_", " ")

    # Identify temperature-related features (case-insensitive substring match)
    def is_temp(feat: str) -> bool:
        f = feat.lower()
        return any(kw in f for kw in ["temperature", "frost", "hot days"])

    rows = []
    for country in COUNTRIES:
        sub = imp_df[imp_df["country"] == country].copy()
        if sub.empty:
            continue

        sub = sub.sort_values("rank").reset_index(drop=True)

        top3 = sub.head(3)["feature_display"].tolist()
        while len(top3) < 3:
            top3.append("—")

        # Find best-ranked temperature feature
        temp_sub = sub[sub["feature_display"].apply(is_temp)]
        if temp_sub.empty:
            top_temp       = "—"
            top_temp_rank  = "—"
        else:
            best_temp_row  = temp_sub.iloc[0]
            top_temp       = best_temp_row["feature_display"]
            top_temp_rank  = int(best_temp_row["rank"])

        rows.append({
            "Country":          country,
            "Rank 1":           top3[0],
            "Rank 2":           top3[1],
            "Rank 3":           top3[2],
            "Top Temp Feature": top_temp,
            "Temp Rank":        top_temp_rank,
        })

    summary_df = pd.DataFrame(rows)

    # Save CSV
    csv_path = os.path.join(save_dir, "shap_feature_summary_table.csv")
    summary_df.to_csv(csv_path, index=False)
    print(f"  Saved: feature_summary/shap_feature_summary_table.csv")

    # Render as matplotlib table PNG
    col_headers = list(summary_df.columns)
    cell_data   = summary_df.values.tolist()
    n_rows      = len(cell_data)
    n_cols      = len(col_headers)

    fig, ax = plt.subplots(figsize=(13, 0.55 * (n_rows + 2)))
    ax.axis("off")

    tbl = ax.table(
        cellText=cell_data,
        colLabels=col_headers,
        cellLoc="center",
        loc="center",
    )
    tbl.auto_set_font_size(False)
    tbl.set_fontsize(9.5)
    tbl.scale(1, 1.8)

    # Header styling
    for col_idx in range(n_cols):
        cell = tbl[0, col_idx]
        cell.set_facecolor(COLOR_HEAD)
        cell.set_text_props(color="white", fontweight="bold")

    # Alternating row backgrounds; highlight Temp Rank column
    for row_idx in range(1, n_rows + 1):
        bg = "#F7F7F7" if row_idx % 2 == 0 else "white"
        for col_idx in range(n_cols):
            tbl[row_idx, col_idx].set_facecolor(bg)
        # Highlight Top Temp Feature + Temp Rank columns in light gold
        for col_idx in [n_cols - 2, n_cols - 1]:
            tinted = "#FEF9EC" if row_idx % 2 == 0 else "#FFFCF2"
            tbl[row_idx, col_idx].set_facecolor(tinted)

    ax.set_title(
        "SVR Augmented (Regularized) — SHAP Feature Summary per Country",
        fontsize=11, fontweight="bold", pad=14
    )

    fig.tight_layout()
    png_path = os.path.join(save_dir, "shap_feature_summary_table.png")
    fig.savefig(png_path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved: feature_summary/shap_feature_summary_table.png")


