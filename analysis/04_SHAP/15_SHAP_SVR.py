def clean_svr_param(value):
    if pd.isna(value):
        return None
    if isinstance(value, str):
        v = value.strip()
        if v.lower() in ["scale", "auto", "linear", "rbf", "poly", "sigmoid"]:
            return v.lower()
        try:
            num = float(v)
            return int(num) if num.is_integer() else num
        except ValueError:
            return v
    if isinstance(value, (int, float, np.integer, np.floating)):
        if float(value).is_integer():
            return int(value)
        return float(value)
    return value


def load_best_params(best_params_path: str) -> pd.DataFrame:
    """Load full params table (all feature sets). Filtering is done per run."""
    df = pd.read_csv(best_params_path)
    required_cols = [
        BEST_PARAMS_COUNTRY_COL,
        BEST_PARAMS_FEATURE_SET_COL,
        BEST_PARAMS_KERNEL_COL,
        BEST_PARAMS_C_COL,
        BEST_PARAMS_EPSILON_COL,
        BEST_PARAMS_GAMMA_COL,
    ]
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing columns in BEST_PARAMS_PATH: {missing}")
    # Remove summary average rows
    df = df[~df[BEST_PARAMS_COUNTRY_COL].str.contains("AVG", na=False)].copy()
    return df


def get_country_best_params(best_params_df: pd.DataFrame,
                             country_name: str,
                             feature_set_name: str) -> dict:
    mask = (
        (best_params_df[BEST_PARAMS_COUNTRY_COL] == country_name) &
        (best_params_df[BEST_PARAMS_FEATURE_SET_COL] == feature_set_name)
    )
    row = best_params_df[mask]
    if row.empty:
        raise ValueError(
            f"No best params found for: {country_name} | {feature_set_name}"
        )
    row = row.iloc[0]
    return {
        "kernel":  clean_svr_param(row[BEST_PARAMS_KERNEL_COL]),
        "C":       clean_svr_param(row[BEST_PARAMS_C_COL]),
        "epsilon": clean_svr_param(row[BEST_PARAMS_EPSILON_COL]),
        "gamma":   clean_svr_param(row[BEST_PARAMS_GAMMA_COL]),
        "degree":  clean_svr_param(row[BEST_PARAMS_DEGREE_COL])
                   if BEST_PARAMS_DEGREE_COL in row.index else None,
    }


def build_svr_model(kernel, C, epsilon, gamma=None, degree=None):
    params = {"kernel": kernel, "C": C, "epsilon": epsilon}
    if kernel in {"rbf", "poly"}:
        params["gamma"] = gamma
    if kernel == "poly" and degree is not None:
        params["degree"] = int(degree)
    return SVR(**params)


def prepare_country_data(data: pd.DataFrame,
                          country_name: str,
                          feature_cols: list) -> pd.DataFrame:
    cdf = data[data[COUNTRY_COL] == country_name].sort_values("Year").copy()
    cdf[LAG_TARGET_NAME] = cdf[TARGET_COL].shift(1)
    cols = list(dict.fromkeys(["Year", TARGET_COL] + feature_cols))
    return cdf[cols].dropna().copy().reset_index(drop=True)


def build_importance_df(country_name: str,
                         feature_set_name: str,
                         feature_cols: list,
                         shap_values: np.ndarray) -> pd.DataFrame:
    importance_df = pd.DataFrame({
        "feature":        feature_cols,
        "mean_abs_shap":  np.abs(shap_values).mean(axis=0),
    }).sort_values("mean_abs_shap", ascending=False).reset_index(drop=True)
    importance_df["country"]     = country_name
    importance_df["feature_set"] = feature_set_name
    importance_df["rank"]        = np.arange(1, len(importance_df) + 1)
    return importance_df[["country", "feature_set", "rank", "feature", "mean_abs_shap"]]


def _highlight_temp_labels(ax) -> None:
    """
    Post-process a SHAP plot's y-axis labels to visually distinguish
    temperature-related features from socioeconomic features.

    Approach: set an orange bounding box on any y-tick label whose text
    matches a temperature-related keyword.  This works for both the SHAP
    bar chart and the SHAP summary dot plot because both use the y-axis
    to display feature names.

    Temperature features identified by substring match (case-insensitive):
      'temperature', 'frost', 'hot days'

    Visual spec
    -----------
    Box face colour : #FFE4CC  (very light orange, low contrast so text stays readable)
    Box edge colour : #E8762C  (warm orange, matches your existing TEMP_COLOR palette)
    Text colour     : #C45A00  (darker orange for legibility on the light background)
    """
    temp_keywords = ["temperature", "frost", "hot days"]
    for label in ax.get_yticklabels():
        text = label.get_text().lower()
        if any(kw in text for kw in temp_keywords):
            label.set_color("#C45A00")
            label.set_bbox({
                "facecolor": "#FFE4CC",
                "edgecolor": "#E8762C",
                "boxstyle":  "round,pad=0.25",
                "linewidth": 0.8,
            })


def save_shap_outputs(country_name: str,
                       feature_set_name: str,
                       output_dir: str,
                       X_scaled_df: pd.DataFrame,
                       shap_values: np.ndarray,
                       importance_df: pd.DataFrame) -> None:
    """
    Save all SHAP-related outputs for one country × feature-set run.

    File routing
    ------------
    shap_plots/   : SHAP summary dot plot + SHAP bar chart (PNG)
    data/         : X used for SHAP (CSV), raw SHAP values (.npy),
                    feature importance table (CSV)

    Temperature feature highlighting
    ---------------------------------
    Both SHAP charts have temperature-related y-axis labels highlighted
    with an orange bounding box (via _highlight_temp_labels) so readers
    can immediately locate these features regardless of their rank position.
    """
    country_safe = country_name.replace(" ", "_")
    tag = f"{country_safe}_{feature_set_name}"

    shap_dir = os.path.join(output_dir, "shap_plots")
    data_dir = os.path.join(output_dir, "data")
    ensure_dir(shap_dir)
    ensure_dir(data_dir)

    # ── data/ ─────────────────────────────────────────────────────────
    # Raw inputs / outputs kept in data/ for reproducibility and auditing
    X_scaled_df.to_csv(os.path.join(data_dir, f"{tag}_X_for_shap.csv"), index=False)
    np.save(os.path.join(data_dir, f"{tag}_shap_values.npy"), shap_values)
    importance_df.to_csv(os.path.join(data_dir, f"{tag}_shap_importance.csv"), index=False)

    feature_label = "Baseline" if feature_set_name == "baseline" else "Augmented"

    # ── shap_plots/ ────────────────────────────────────────────────────
    # 4. SHAP summary dot plot (shows direction of feature effects)
    plt.figure(figsize=(12, 7))
    shap.summary_plot(shap_values, X_scaled_df, show=False)
    ax = plt.gca()
    _highlight_temp_labels(ax)          # ← highlight temperature y-labels
    plt.title(f"{country_name} ({feature_label}) - SVR SHAP Summary")
    plt.tight_layout()
    plt.savefig(
        os.path.join(shap_dir, f"{tag}_shap_summary.png"), dpi=300, bbox_inches="tight"
    )
    plt.close()

    # 5. SHAP bar chart (ranked mean |SHAP| — global feature importance)
    plt.figure(figsize=(12, 7))
    shap.summary_plot(shap_values, X_scaled_df, plot_type="bar", show=False)
    ax = plt.gca()
    _highlight_temp_labels(ax)          # ← highlight temperature y-labels
    plt.title(f"{country_name} ({feature_label}) - SVR SHAP Feature Importance")
    plt.xlabel("Mean(|SHAP value|)")
    plt.tight_layout()
    plt.savefig(
        os.path.join(shap_dir, f"{tag}_shap_bar.png"), dpi=300, bbox_inches="tight"
    )
    plt.close()


def save_morf_lerf_curve(country_name: str,
                          feature_set_name: str,
                          output_dir: str,
                          model,
                          X_scaled_df: pd.DataFrame,
                          y_scaled: np.ndarray,
                          shap_values: np.ndarray,
                          importance_df: pd.DataFrame) -> pd.DataFrame:
    """
    Compute and save MoRF/LeRF (Most/Least Relevant First) curves.

    MoRF removes features in order of DECREASING SHAP importance (most → least).
    LeRF removes features in order of INCREASING SHAP importance (least → most).
    A large gap between the two curves confirms that the SHAP ranking is meaningful.

    All outputs (CSV + PNG) are saved to morf_lerf/.
    """
    morf_dir = os.path.join(output_dir, "morf_lerf")
    ensure_dir(morf_dir)

    country_safe  = country_name.replace(" ", "_")
    tag           = f"{country_safe}_{feature_set_name}"
    feature_label = "Baseline" if feature_set_name == "baseline" else "Augmented"

    X_original    = X_scaled_df.copy()
    y_scaled      = np.asarray(y_scaled).reshape(-1)
    feature_names = X_original.columns.to_list()

    ranked_features         = importance_df.sort_values("rank")["feature"].to_list()
    ranked_features_display = [f.replace("_", " ") for f in ranked_features]
    morf_order = ranked_features_display
    lerf_order = list(reversed(ranked_features_display))

    mask_values = X_original.mean(axis=0)

    baseline_pred = model.predict(X_original)
    baseline_mae  = mean_absolute_error(y_scaled, baseline_pred)
    baseline_rmse = np.sqrt(mean_squared_error(y_scaled, baseline_pred))

    curve_rows = [{
        "country":              country_name,
        "feature_set":          feature_set_name,
        "n_features_masked":    0,
        "MORF_MAE_scaled_y":    baseline_mae,
        "LERF_MAE_scaled_y":    baseline_mae,
        "MORF_RMSE_scaled_y":   baseline_rmse,
        "LERF_RMSE_scaled_y":   baseline_rmse,
        "MORF_masked_features": "",
        "LERF_masked_features": "",
    }]

    morf_mae_values  = [baseline_mae]
    lerf_mae_values  = [baseline_mae]
    morf_rmse_values = [baseline_rmse]
    lerf_rmse_values = [baseline_rmse]
    steps = [0]

    for k in range(1, len(feature_names) + 1):
        steps.append(k)

        X_morf = X_original.copy()
        for f in morf_order[:k]:
            X_morf[f] = mask_values[f]

        X_lerf = X_original.copy()
        for f in lerf_order[:k]:
            X_lerf[f] = mask_values[f]

        morf_pred = model.predict(X_morf)
        lerf_pred = model.predict(X_lerf)

        morf_mae  = mean_absolute_error(y_scaled, morf_pred)
        lerf_mae  = mean_absolute_error(y_scaled, lerf_pred)
        morf_rmse = np.sqrt(mean_squared_error(y_scaled, morf_pred))
        lerf_rmse = np.sqrt(mean_squared_error(y_scaled, lerf_pred))

        morf_mae_values.append(morf_mae)
        lerf_mae_values.append(lerf_mae)
        morf_rmse_values.append(morf_rmse)
        lerf_rmse_values.append(lerf_rmse)

        curve_rows.append({
            "country":              country_name,
            "feature_set":          feature_set_name,
            "n_features_masked":    k,
            "MORF_MAE_scaled_y":    morf_mae,
            "LERF_MAE_scaled_y":    lerf_mae,
            "MORF_RMSE_scaled_y":   morf_rmse,
            "LERF_RMSE_scaled_y":   lerf_rmse,
            "MORF_masked_features": "; ".join(morf_order[:k]),
            "LERF_masked_features": "; ".join(lerf_order[:k]),
        })

    curve_df = pd.DataFrame(curve_rows)
    curve_df.to_csv(os.path.join(morf_dir, f"{tag}_morf_lerf_curve.csv"), index=False)

    # ── MAE curve with gap shading ────────────────────────────────────
    fig, ax = plt.subplots(figsize=(9, 6))
    ax.plot(steps, morf_mae_values, marker="o", color="#2271B3", label="MORF")
    ax.plot(steps, lerf_mae_values, marker="o", color="#F28E2B", label="LERF")
    ax.fill_between(
        steps, morf_mae_values, lerf_mae_values,
        alpha=0.15, color="#2271B3", label="MORF–LERF gap"
    )
    ax.set_xlabel("Number of masked features")
    ax.set_ylabel("MAE on scaled y")
    ax.set_title(f"{country_name} ({feature_label}) - MORF vs LERF Curve (MAE)")
    ax.legend()
    ax.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(
        os.path.join(morf_dir, f"{tag}_morf_lerf_curve_mae.png"),
        dpi=300, bbox_inches="tight"
    )
    plt.close()

    # ── RMSE curve with gap shading ───────────────────────────────────
    fig, ax = plt.subplots(figsize=(9, 6))
    ax.plot(steps, morf_rmse_values, marker="o", color="#2271B3", label="MORF")
    ax.plot(steps, lerf_rmse_values, marker="o", color="#F28E2B", label="LERF")
    ax.fill_between(
        steps, morf_rmse_values, lerf_rmse_values,
        alpha=0.15, color="#2271B3", label="MORF–LERF gap"
    )
    ax.set_xlabel("Number of masked features")
    ax.set_ylabel("RMSE on scaled y")
    ax.set_title(f"{country_name} ({feature_label}) - MORF vs LERF Curve (RMSE)")
    ax.legend()
    ax.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(
        os.path.join(morf_dir, f"{tag}_morf_lerf_curve_rmse.png"),
        dpi=300, bbox_inches="tight"
    )
    plt.close()

    return curve_df


def run_svr_shap_for_country(data: pd.DataFrame,
                               best_params_df: pd.DataFrame,
                               country_name: str,
                               feature_set_name: str,
                               feature_cols: list,
                               target_col: str,
                               output_dir: str) -> dict:
    print(f"\n===== {country_name} | {feature_set_name} =====")

    country_df = prepare_country_data(data, country_name, feature_cols)
    if country_df.empty:
        raise ValueError(f"All rows dropped for: {country_name}")
    print(f"  Rows for SHAP: {len(country_df)}")

    X = country_df[feature_cols].copy()
    display_names = [col.replace("_", " ") for col in X.columns]
    X.columns = display_names
    y = country_df[target_col].copy()

    sc_x = MinMaxScaler(feature_range=SCALE_RANGE)
    sc_y = MinMaxScaler(feature_range=SCALE_RANGE)
    X_scaled    = sc_x.fit_transform(X)
    y_scaled    = sc_y.fit_transform(y.values.reshape(-1, 1)).reshape(-1)
    X_scaled_df = pd.DataFrame(X_scaled, columns=display_names)

    best_params = get_country_best_params(best_params_df, country_name, feature_set_name)
    print(f"  Best params: {best_params}")

    model = build_svr_model(**best_params)
    model.fit(X_scaled, y_scaled)

    explainer   = shap.KernelExplainer(model.predict, X_scaled_df)
    shap_values = np.array(explainer.shap_values(X_scaled_df))

    importance_df = build_importance_df(
        country_name, feature_set_name, feature_cols, shap_values
    )

    save_shap_outputs(
        country_name, feature_set_name, output_dir,
        X_scaled_df, shap_values, importance_df
    )

    morf_lerf_df = save_morf_lerf_curve(
        country_name, feature_set_name, output_dir,
        model, X_scaled_df, y_scaled, shap_values, importance_df
    )

    print(f"  Done: {country_name} | {feature_set_name}")

    return {
        "country":                          country_name,
        "feature_set":                      feature_set_name,
        "status":                           "success",
        "n_rows_used":                      len(country_df),
        "kernel":                           best_params["kernel"],
        "C":                                best_params["C"],
        "epsilon":                          best_params["epsilon"],
        "gamma":                            best_params["gamma"],
        "degree":                           best_params["degree"],
        "morf_lerf_final_MORF_MAE_scaled_y": morf_lerf_df["MORF_MAE_scaled_y"].iloc[-1],
        "morf_lerf_final_LERF_MAE_scaled_y": morf_lerf_df["LERF_MAE_scaled_y"].iloc[-1],
        "error_message":                    "",
    }


